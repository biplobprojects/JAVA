public class BreakAndContinue {
    public static void main(String[] args) {
        for(int i = 0;i<=20; i++){
            
            //BREAK STATEMENT:
             if(i== 10)
                 break;
 
            //CONTINUE STATEMENT:
                if (i==10)
                continue;
                
            
            System.out.println(i);
        }
    }
}
